#英文首字母大写
name = "ada lovelace"
print(name.title())

#大写和小写
name = "Ada Lovelace"
print(name.upper())
print(name.lower())

#字符拼接
first_name = "ada"
last_name = "lovelace"
full_name = first_name + " " + last_name
print(full_name)

#字符拼接和首字母大写
first_name = "ada"
last_name = "lovelace"
full_name = first_name + " " + last_name
print("Hello, " + full_name.title() + "!")

#使用变量打印
first_name = "ada"
last_name = "lovelace"
full_name = first_name + " " + last_name
message = "Hello, " + full_name.title() + "!"
print(message)
